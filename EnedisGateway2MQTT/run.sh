#!/usr/bin/env bashio

bashio::log.info "Generating config.yaml from options.json"
echo '# Generated by homeassistant, do not edit!' > /usr/src/EnedisGateway2MQTT/enedisGateway2MQTT/config/config.yaml.tmp
echo '# Edit configuration only at the Add-on configuration tab!' >> /usr/src/EnedisGateway2MQTT/enedisGateway2MQTT/config/config.yaml.tmp
json2yaml /data/options.json >> /usr/src/EnedisGateway2MQTT/enedisGateway2MQTT/config/config.yaml.tmp
sed 's/  -/    -/'  /usr/src/EnedisGateway2MQTT/enedisGateway2MQTT/config/config.yaml.tmp > /usr/src/EnedisGateway2MQTT/enedisGateway2MQTT/config/config.yaml
bashio::log.info "Configuration :"
bashio::log.info "$(cat /usr/src/EnedisGateway2MQTT/enedisGateway2MQTT/config/config.yaml)"

debug=""
if $(bashio::config.true debug); then
    bashio::log.info "Will be started in debug mode"
    debug="-v"
fi

bashio::log.info "Image build"
bashio::log.info "starting original stuff..."
python3 /usr/src/EnedisGateway2MQTT/enedisGateway2MQTT/main.py $debug
